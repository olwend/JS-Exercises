// Switch

let animal = "XDOG"

switch(animal.toLowerCase()) {
  case "dog":
    console.log("WOOF!");
    break;
  case "snake":
    console.log("HISSSS");
    break;
  case "cat":
    console.log("MEOW");
    break;
  case "cow":
    console.log("MOO");
    break;
  default:
    console.log ("Unrecognizable animal :(");
    break
}

